static void animate(uint8_t x, uint8_t y) {
  frame += 1;

  oled_set_cursor(x, y);
  oled_write_raw_P(boot[abs(1 - frame)], _ANIM_BYTES);
}

static void animate_clear(uint8_t x, uint8_t y) {
  oled_set_cursor(x, y);
  oled_write("                    ", false);
}

void bootanim_render(uint8_t x, uint8_t y) {
  if (!is_bootanim_done && timer_elapsed32(anim_timer) > _ANIM_FRAME_DURATION) {
    anim_timer = timer_read32();

    if (frame < _ANIM_FRAMES) {
      animate(x, y);
    } else {
      animate_clear(x, y);
      is_bootanim_done = true;
    }
  }
}
